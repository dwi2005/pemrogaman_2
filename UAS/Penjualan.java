    private void loadComboProduk() {
    // Method untuk mengambil data produk dari database ke ComboBox
        try {
            String sql = "SELECT id_produk, nama_produk FROM produk";
            // Query mengambil id dan nama produk
            PreparedStatement pst = connect.con.prepareStatement(sql);
            // Menyiapkan query SQL
            ResultSet rs = pst.executeQuery();
            // Menjalankan query SELECT

            jComboBox1.removeAllItems();
            // Mengosongkan isi ComboBox

            while (rs.next()) {
            // Loop selama data masih ada
                jComboBox1.addItem(rs.getInt("id_produk") + " - " + rs.getString("nama_produk"));
                // Menambahkan data produk ke ComboBox
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            // Menampilkan pesan error jika gagal
        }
    }

    private void tampilTabelPenjualan() {
    // Method untuk menampilkan data penjualan ke JTable
        try {
            String sql = "SELECT "
                    + "pnj.id_penjualan AS 'ID Penjualan', "
                    + "prd.nama_produk AS 'Nama Produk', "
                    + "pnj.jumlah AS 'Jumlah', "
                    + "(pnj.jumlah * prd.harga) AS 'Total' "
                    + "FROM penjualan pnj "
                    + "JOIN produk prd ON pnj.id_produk = prd.id_produk";
                    // Query join tabel penjualan dan produk

            PreparedStatement pst = connect.con.prepareStatement(sql);
            // Menyiapkan query
            ResultSet rs = pst.executeQuery();
            // Menjalankan query

            readTotal.setModel(DbUtils.resultSetToTableModel(rs));
            // Menampilkan hasil query ke JTable

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            // Menampilkan pesan error
        }
    }

