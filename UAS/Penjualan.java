    private void simpanDatasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanDatasActionPerformed
    // Method untuk menyimpan data penjualan ke database
        try {
            String item = jComboBox1.getSelectedItem().toString();
            // Mengambil item yang dipilih dari ComboBox
            int idProduk = Integer.parseInt(item.split(" - ")[0]);
            // Mengambil id produk dari ComboBox
            int jumlah = Integer.parseInt(jumlahdats.getText());
            // Mengambil jumlah yang diinput user

            String sql = "INSERT INTO penjualan (id_produk, jumlah) VALUES (?, ?)";
            // Query insert data penjualan
            PreparedStatement pst = connect.con.prepareStatement(sql);
            // Menyiapkan query insert

            pst.setInt(1, idProduk);
            // Mengisi parameter id produk
            pst.setInt(2, jumlah);
            // Mengisi parameter jumlah
            pst.executeUpdate();
            // Menjalankan perintah insert

            pst.close();
            // Menutup PreparedStatement
            
            JOptionPane.showMessageDialog(null, "Transaksi berhasil disimpan");
            // Menampilkan pesan berhasil

            tampilTabelPenjualan();
            // Refresh data tabel
            jumlahdats.setText("");
            // Mengosongkan field jumlah

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            // Menampilkan pesan error
        }
    }//GEN-LAST:event_simpanDatasActionPerformed

