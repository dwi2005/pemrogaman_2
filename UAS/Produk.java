    private void updateProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProActionPerformed
        // TODO add your handling code here:
        if (nama.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Produk belum diisi!");
            return;
        }
        // Validasi nama
        
        String namaMasuk = nama.getText().trim();
        int harga;
        try {
            harga = Integer.parseInt(hargamasuk.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!");
            return;
        }

        int stok;
        try {
            stok = Integer.parseInt(stokmasuk.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Stok harus berupa angka!");
            return;
        }
        
        int id;
        try {
            id = Integer.parseInt(Id.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukan Kata Kunci Hapus!");
            return;
        }
        // Validasi ID
        
        updateP.updateProduk(id, namaMasuk, harga, stok);
        // Update data ke database
        tableProduk();
        // Refresh tabel
    }//GEN-LAST:event_updateProActionPerformed

