    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (nama.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Produk belum diisi!");
            return;
        }
        // Validasi nama produk
        
        String namaMasuk = nama.getText().trim();
        int harga;
        try {
            harga = Integer.parseInt(hargamasuk.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!");
            return;
        }
        // Validasi harga
        
        int stok;
        try {
            stok = Integer.parseInt(stokmasuk.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Stok harus berupa angka!");
            return;
        }
        // Validasi stok

        tambahProduk.insertProduk(namaMasuk, harga, stok);
        // Simpan data ke database
        tableProduk();
        // Refresh tabel
    }//GEN-LAST:event_jButton4ActionPerformed

