    private void readProdukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_readProdukMouseClicked
        // TODO add your handling code here:
        int row = readProduk.getSelectedRow();
        // Mengambil baris yang dipilih
        if (row == -1) {
            return;
        }
        String id = readProduk.getValueAt(row, 0).toString();
        String namat = readProduk.getValueAt(row, 1).toString();
        String harga = readProduk.getValueAt(row, 2).toString();
        String stok = readProduk.getValueAt(row, 3).toString();
        // Mengambil data dari tabel

        nama.setText(namat);
        hargamasuk.setText(harga);
        stokmasuk.setText(stok);
        Id.setText(id);
        // Menampilkan data ke textfield

    }//GEN-LAST:event_readProdukMouseClicked

