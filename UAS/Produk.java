    private void tableProduk() {
        try {
            String sql = "SELECT "
                    + "id_produk AS 'ID Produk', "
                    + "nama_produk AS 'Nama Produk', "
                    + "harga AS 'Harga', "
                    + "stok AS 'Stok' "
                    + "FROM produk";
                    // Query untuk mengambil data produk

            PreparedStatement pst = connect.con.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            // Menjalankan query SELECT

            readProduk.setModel(DbUtils.resultSetToTableModel(rs));
            // Menampilkan data ke JTable

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            // Menampilkan pesan error
        }
    }

